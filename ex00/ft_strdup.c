#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	char *tab;

	tab = (char*)malloc(sizeof(*src) * ft_strlen(src + 1));
	if (tab != NULL)
	{
		ft_strcpy(tab, src);
		return (tab);
	}
	return (NULL);
}
